export function formatRelativeMinutes(locale, value) {
  if (typeof value === "string") return value;
  if (value === 0) return t(locale, "now");
  return locale === "zh" ? `${value} ${t(locale, "minutesAgo")}` : `${value}${t(locale, "minutesAgo")}`;
}
